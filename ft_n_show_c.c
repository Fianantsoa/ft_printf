int	ft_n_show_c(long n, int base)
{
	int	len;

	len = 0;
	if (n == 0)
		return (1);
	if (base == 10 && n < 0)
	{
		len++;
		n = -n;
	}
	while (n > 0)
	{
		n /= base;
		len++;
	}
	return (len);
}
